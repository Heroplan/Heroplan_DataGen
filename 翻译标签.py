def remove_japanese_names(data):
    for hero_name in data:
        if "日语名" in data[hero_name]:
            del data[hero_name]["日语名"]
    return data
def translate_data(data):
    translated_data = {}
    for hero_name, hero_data in data.items():
        translated_hero = {}
        for key, value in hero_data.items():
            # 翻译键名（如"基本能力" -> "基础技能"）
            translated_key = TRANSLATION_DICT.get(key, key)
            
            # 如果是列表，翻译每个项目
            if isinstance(value, list):
                translated_value = [TRANSLATION_DICT.get(item, item) for item in value]
            else:
                translated_value = TRANSLATION_DICT.get(value, value)
            
            translated_hero[translated_key] = translated_value
        
        translated_data[hero_name] = translated_hero
    
    return translated_data

